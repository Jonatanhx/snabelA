  public generateLevel(id: number, game: CurrentActiveMenu): Level {
    const levelDesign = this.levelDesigns[id - 1];
    const entities: Entity[] = [];
    const blockSize = height / levelDesign.length;
    const velocityX = 10;

    for (let y = 0; y < levelDesign.length; y++) {
      for (let x = 0; x < levelDesign[y].length; x++) {
        const siffra = levelDesign[y][x]; // Hämta siffran från nivådesignen på´dens aktuella positionen

        // PLATFORM TOP
        if (siffra === 1) {
          entities.push(
            new Platform(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.platformTopImg
            )
          );
        }
        // PLATFORM BOTTOM
        if (siffra === 2) {
          entities.push(
            new Platform(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.platformBottomImg
            )
          );
        }
        // CACTUS - LOW -THICKER ONE
        if (siffra === 3) {
          entities.push(
            new Obstacle(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.obstacleImgCactusLow
            )
          );
        }
        // OBSTACLE - CACTUS MEDIUM - DOUBLE
        if (siffra === 4) {
          entities.push(
            new Obstacle(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.obstacleImgCactusMedium
            )
          );
        }
        // OBSTACLE CACTUS HIGH - SINGLE
        if (siffra === 5) {
          entities.push(
            new Obstacle(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.obstacleImgCactusHigh
            )
          );
        }
        // OBSTACLE - ROOTS LOW
        if (siffra === 6) {
          entities.push(
            new Obstacle(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.obstacleImgRoot
            )
          );
        }
        // OBSTACLE - BOX
        if (siffra === 7) {
          entities.push(
            new Obstacle(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              entityImages.obstacleImgBox
            )
          );
        }
        //PLAYER
        if (siffra === 8) {
          const ratio = 2;
          //STYRA HUR STOR JALAMAN ÄR
          const width = blockSize * 0.81;
          const height = width * ratio;
          const additionalHeigth = height - blockSize;
          entities.push(
            new Player(
              x * blockSize,
              y * blockSize - additionalHeigth,
              width,
              height,
              null as any
            )
          );
        }
        // GOAL
        if (siffra === 9) {
          entities.push(
            new Goal(
              x * blockSize,
              y * blockSize,
              blockSize,
              blockSize,
              null as any
            )
          );
        }
      }
    }

    const levelLength = levelDesign[0].length * blockSize;
    const progressBar = new ProgressBar(levelLength, velocityX);
    return new Level(id, game, entities, progressBar);
  }
